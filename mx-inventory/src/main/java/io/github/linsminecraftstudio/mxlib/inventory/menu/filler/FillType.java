package io.github.linsminecraftstudio.mxlib.inventory.menu.filler;

public enum FillType {
    /**
     * Only fill the top line.
     */
    TOP_LINE,
    /**
     * Fill in the entire menu.
     */
    FULL,
    /**
     * Only fill the bottom line.
     */
    BOTTOM_LINE,
    /**
     * Fill in the entire menu, excluding the top and bottom line.
     */
    NO_TOP_AND_BOTTOM
}
